#include "libft.h"

int		ft_atoi(const char *str)
{
	int f;
	int s;

	s = 1;
	f = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '-' && *(str + 1) >= '0' && *(str + 1) <= '9')
	{
		s = -1;
		str++;
	}
	if (*str == '+' && *(str + 1) >= '0' && *(str + 1) <= '9')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		f = f * 10 + *str - '0';
		str++;
	}
	return (f * s);
}
