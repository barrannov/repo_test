#include "libft.h"

char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	int i;
	int c;

	c = 0;
	i = 0;
	while (s1[i])
	{
		i++;
	}
	while (s2[c])
	{
		s1[i] = s2[c];
		c++;
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
