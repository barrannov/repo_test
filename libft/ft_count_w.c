#include "libft.h"

int		ft_count_w(char const *s, char c)
{
	char	*p;
	int		cw;

	if (!s)
		return (0);
	cw = 0;
	while (*s)
	{
		(*s == c && *s) ? s++ : 0;
		p = (char *)s;
		while (*p != c && *p)
			p++;
		(*s != c && *s) ? cw++ : 0;
		s = p;
	}
	return (cw);
}
